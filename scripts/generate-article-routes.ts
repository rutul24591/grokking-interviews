#!/usr/bin/env node

/**
 * Generate Article Routes Manifest
 * 
 * Scans content/articles/ and generates lib/article-routes.ts
 * 
 * Usage: pnpm tsx scripts/generate-article-routes.ts
 */

import * as fs from "fs";
import * as path from "path";

const ARTICLES_DIR = path.join(process.cwd(), "content", "articles");
const OUTPUT_FILE = path.join(process.cwd(), "lib", "article-routes.ts");

// Map filesystem paths to sidebar slugs
const PATH_MAPPINGS: Record<string, string> = {
  "system-design/frontend": "system-design-concepts/frontend-concepts",
  "system-design/backend": "system-design-concepts/backend-concepts",
  "requirements/functional-requirements": "requirements/functional-requirements",
  "requirements/non-functional-requirements": "requirements/non-functional-requirements",
};

function main() {
  console.log("🔍 Scanning article files...\n");

  const routes: string[] = [];
  let count = 0;

  // Walk through all article directories
  for (const [fsPath, sidebarPath] of Object.entries(PATH_MAPPINGS)) {
    const fullDir = path.join(ARTICLES_DIR, fsPath);
    
    if (!fs.existsSync(fullDir)) {
      console.warn(`⚠️  Directory not found: ${fullDir}`);
      continue;
    }

    // Recursively find all .tsx files
    const files = findAllTsxFiles(fullDir);
    
    for (const file of files) {
      const relativePath = path.relative(fullDir, file).replace(/\.tsx$/, '');
      const parts = relativePath.split(path.sep);
      
      // Build sidebar key: category/subcategory/topic
      const sidebarKey = `${sidebarPath}/${parts.join("/")}`;
      
      // Build import path
      const importPath = `@/content/articles/${fsPath}/${relativePath}`;
      
      // Generate route entry
      routes.push(`  "${sidebarKey}": () => import("${importPath}"),`);
      count++;
    }
  }

  // Generate the manifest file
  const manifest = `/**
 * Article Routes Manifest
 * 
 * AUTO-GENERATED by scripts/generate-article-routes.ts
 * DO NOT EDIT MANUALLY
 * 
 * To update: run \`pnpm generate-article-routes\`
 */

export const articleRoutes: Record<string, () => Promise<any>> = {
${routes.join("\n")}
};

/**
 * Get all article paths for static generation
 */
export function getAllArticlePaths() {
  return Object.keys(articleRoutes).map((key) => {
    const parts = key.split("/");
    return {
      domain: parts[0],
      category: parts[1],
      subcategory: parts[2],
      topic: parts[3],
    };
  });
}

/**
 * Load an article by its path
 */
export async function loadArticle(domain: string, category: string, subcategory: string, topic: string) {
  const key = \`\${domain}/\${category}/\${subcategory}/\${topic}\`;
  const loadModule = articleRoutes[key];

  if (!loadModule) {
    return null;
  }

  try {
    const module = await loadModule();
    return {
      metadata: module.metadata,
      component: module.default,
    };
  } catch {
    return null;
  }
}
`;

  fs.writeFileSync(OUTPUT_FILE, manifest, "utf-8");

  console.log("=".repeat(60));
  console.log("📊 SUMMARY");
  console.log("=".repeat(60));
  console.log(`Articles scanned:  ${count}`);
  console.log(`Output: ${path.relative(process.cwd(), OUTPUT_FILE)}`);
  console.log(`Size: ${(fs.statSync(OUTPUT_FILE).size / 1024).toFixed(2)} KB`);
  console.log("\n✅ Article routes manifest generated successfully!");
  console.log("\n📝 Next steps:");
  console.log("   - Run 'pnpm build' to rebuild the app");
  console.log("");
}

function findAllTsxFiles(dir: string): string[] {
  const files: string[] = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    
    if (entry.isDirectory()) {
      files.push(...findAllTsxFiles(fullPath));
    } else if (entry.isFile() && entry.name.endsWith(".tsx")) {
      files.push(fullPath);
    }
  }

  return files;
}

main();
