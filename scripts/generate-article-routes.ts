#!/usr/bin/env node

/**
 * Generate Article Routes Manifest
 * 
 * Scans content/articles/ and generates lib/article-routes.ts
 * 
 * Usage: pnpm tsx scripts/generate-article-routes.ts
 */

import * as fs from "fs";
import * as path from "path";

const ARTICLES_DIR = path.join(process.cwd(), "content", "articles");
const OUTPUT_FILE = path.join(process.cwd(), "lib", "article-routes.ts");

// Map filesystem paths to sidebar slugs
const PATH_MAPPINGS: Record<string, string> = {
  "system-design/frontend": "system-design-concepts/frontend-concepts",
  "system-design/backend": "system-design-concepts/backend-concepts",
  "requirements/functional-requirements": "requirements/functional-requirements",
  "requirements/non-functional-requirements": "requirements/non-functional-requirements",
  "system-design-problems/high-level-design": "system-design-problems/high-level-design",
  "system-design-problems/low-level-design": "system-design-problems/low-level-design",
};

// Map sidebar slugs back to filesystem paths (for subcategory manifest)
const SIDEBAR_TO_FS_MAP: Record<string, string> = {
  "system-design-concepts/frontend-concepts": "system-design/frontend",
  "system-design-concepts/backend-concepts": "system-design/backend",
  "requirements/functional-requirements": "requirements/functional-requirements",
  "requirements/non-functional-requirements": "requirements/non-functional-requirements",
  "system-design-problems/high-level-design": "system-design-problems/high-level-design",
  "system-design-problems/low-level-design": "system-design-problems/low-level-design",
};

function main() {
  console.log("🔍 Scanning article files...\n");

  const routes: string[] = [];
  const subcategoryArticles: Map<string, Array<{slug: string, title: string, description: string}>> = new Map();
  let count = 0;

  // Walk through all article directories
  for (const [fsPath, sidebarPath] of Object.entries(PATH_MAPPINGS)) {
    const fullDir = path.join(ARTICLES_DIR, fsPath);

    if (!fs.existsSync(fullDir)) {
      console.warn(`⚠️  Directory not found: ${fullDir}`);
      continue;
    }

    // Recursively find all .tsx files
    const files = findAllTsxFiles(fullDir);

    for (const file of files) {
      const relativePath = path.relative(fullDir, file).replace(/\.tsx$/, '');
      const parts = relativePath.split(path.sep);

      // Build sidebar key: category/subcategory/topic
      const sidebarKey = `${sidebarPath}/${parts.join("/")}`;

      // Build import path
      const importPath = `@/content/articles/${fsPath}/${relativePath}`;

      // Generate route entry
      routes.push(`  "${sidebarKey}": () => import("${importPath}"),`);
      count++;

      // Extract metadata for subcategory manifest
      const content = fs.readFileSync(file, 'utf-8');
      const titleMatch = content.match(/title:\s*"([^"]*)"/);
      const descMatch = content.match(/description:\s*"([^"]*)"/);

      // Build subcategory key using sidebar path format: domain/category/subcategory
      // parts[0] = subcategory, parts[1] = topic
      // sidebarPath already includes domain/category (e.g., "system-design-concepts/frontend-concepts")
      const subcategoryKey = `${sidebarPath}/${parts[0]}`;
      if (!subcategoryArticles.has(subcategoryKey)) {
        subcategoryArticles.set(subcategoryKey, []);
      }
      subcategoryArticles.get(subcategoryKey)!.push({
        slug: parts[parts.length - 1],
        title: titleMatch ? titleMatch[1] : parts[parts.length - 1],
        description: descMatch ? descMatch[1] : '',
      });
    }
  }

  // Generate the manifest file
  const manifest = `/**
 * Article Routes Manifest
 *
 * AUTO-GENERATED by scripts/generate-article-routes.ts
 * DO NOT EDIT MANUALLY
 *
 * To update: run \`pnpm generate-article-routes\`
 */

export const articleRoutes: Record<string, () => Promise<any>> = {
${routes.join("\n")}
};

/**
 * Get all article paths for static generation
 */
export function getAllArticlePaths() {
  return Object.keys(articleRoutes).map((key) => {
    const parts = key.split("/");
    return {
      domain: parts[0],
      category: parts[1],
      subcategory: parts[2],
      topic: parts[3],
    };
  });
}

/**
 * Load an article by its path
 */
export async function loadArticle(domain: string, category: string, subcategory: string, topic: string) {
  const key = \`\${domain}/\${category}/\${subcategory}/\${topic}\`;
  const loadModule = articleRoutes[key];

  if (!loadModule) {
    return null;
  }

  try {
    const module = await loadModule();
    return {
      metadata: module.metadata,
      component: module.default,
    };
  } catch {
    return null;
  }
}
`;

  // Generate subcategory manifest
  const subcategoryManifest: Record<string, Array<{slug: string, title: string, description: string}>> = {};
  for (const [key, articles] of subcategoryArticles.entries()) {
    subcategoryManifest[key] = articles;
  }

  const subcategoryManifestFile = `/**
 * Subcategory Articles Manifest
 *
 * AUTO-GENERATED by scripts/generate-article-routes.ts
 * DO NOT EDIT MANUALLY
 *
 * To update: run \`pnpm generate-article-routes\`
 */

export const subcategoryArticles: Record<string, Array<{slug: string; title: string; description: string}>> = ${JSON.stringify(subcategoryManifest, null, 2)};
`;

  const SUBCATEGORY_OUTPUT_FILE = path.join(process.cwd(), "lib", "subcategory-articles.ts");
  fs.writeFileSync(SUBCATEGORY_OUTPUT_FILE, subcategoryManifestFile, "utf-8");

  fs.writeFileSync(OUTPUT_FILE, manifest, "utf-8");

  console.log("=".repeat(60));
  console.log("📊 SUMMARY");
  console.log("=".repeat(60));
  console.log(`Articles scanned:  ${count}`);
  console.log(`Output: ${path.relative(process.cwd(), OUTPUT_FILE)}`);
  console.log(`Size: ${(fs.statSync(OUTPUT_FILE).size / 1024).toFixed(2)} KB`);
  console.log("\n✅ Article routes manifest generated successfully!");
  console.log("\n📝 Next steps:");
  console.log("   - Run 'pnpm build' to rebuild the app");
  console.log("");
}

function findAllTsxFiles(dir: string): string[] {
  const files: string[] = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    
    if (entry.isDirectory()) {
      files.push(...findAllTsxFiles(fullPath));
    } else if (entry.isFile() && entry.name.endsWith(".tsx")) {
      files.push(fullPath);
    }
  }

  return files;
}

main();
